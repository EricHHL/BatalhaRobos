    @Override
    public Component getListCellRendererComponent(JList<? extends Robo> list, Robo robo, int index, boolean isSelected, boolean cellHasFocus) {
        
        lblIcon.setIcon(new ImageIcon(getClass().getResource("/imagens/" + robo.getImagem())));
        lblNome.setText(robo.getNome());
        lblVida.setText(robo.getVida()+"");
        lblArmadura.setText(robo.getArmadura()+"");
        
        
        if (isSelected) {
            setBackground(Color.cyan);
            setForeground(list.getSelectionForeground());
        } else {
            setBackground(list.getBackground());
            setForeground(list.getForeground());
        }
        
        return this;
    }


